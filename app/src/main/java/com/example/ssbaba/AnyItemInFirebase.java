package com.example.ssbaba;

public class AnyItemInFirebase {
    String color;
    String description;
    String image;
    String model;
    String name;
    String price;
    String year;

    public String getColor() {
        return color;
    }

    public String getDescription() {
        return description;
    }

    public String getImage() {
        return image;
    }

    public String getModel() {
        return model;
    }

    public String getName() {
        return name;
    }

    public String getPrice() {
        return price;
    }

    public String getYear() {
        return year;
    }
}
